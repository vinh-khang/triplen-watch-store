 <div style="text-align: justify;"><strong>1. Quá trình hình thành và phát triển:</strong></div>


<div style="text-align: justify;"><p> </p><strong> Triplen</strong> ra đời trong thời điểm kinh doanh qua internet tại Việt Nam còn non trẻ, thị trường còn khó khăn. Trong quá trình phát triển, Triplen mở rộng hoạt động kinh doanh đa dạng hơn là kênh phân phối, bán lẻ các thương hiệu đồng hồ danh tiếng. Khách hàng luôn luôn tin tưởng sử dụng sản phẩm và dịch vụ được cung cấp bởi Triplen. Qua đó, Triplen <span style="line-height:1.6">luôn duy trì được tốc độ tăng trưởng cao, toàn diện về mọi mặt một cách bền vững so với các công ty kinh doanh cùng lĩnh vực.</span></div>

<div style="text-align: justify;"> </div>

<div style="text-align: justify;">Với nền tảng vững chắc, Công ty Cổ phần Trực tuyến Vĩnh Khang được thành lập theo quyết định số 0104938104 Sở Kế hoạch &amp; Đầu tư thành phố Hà Nội cấp ngày 07 tháng 10 năm 2010, tên giao dịch tiếng Anh là Vinh Khang Online .,Jsc, chính thức đặt nền móng xây dựng trở <span style="line-height:1.6">thành công ty phân phối, bán lẻ đồng hồ hàng đầu Việt Nam. Và website www.triplen.vn của Công ty Cổ phần Trực tuyến Vĩnh Khang đã trở thành địa chỉ quen thuộc của mọi người khi muốn tìm mua cho mình những sản phẩm đồng hồ hàng hiệu cao cấp.</span></div>

<div style="text-align: justify;"> </div>

<div style="text-align: justify;">Công ty có đội ngũ nhân viên trẻ nhưng am hiểu sâu sắc về nghiệp vụ, chuyên môn cao, đủ khả năng để có thể đáp ứng mọi yêu cầu dù khắt khe nhất của khách hàng. Không những thế, đội ngũ nhân viên của Triplen Watch Store còn là những người đầy lòng nhiệt tình và có thái độ niềm nở trong cung cách phục vụ khách hàng. Triplen cam kết duy trì mối quan hệ đối tác lâu dài, tận tụy với khách hàng. Sự thành công của khách hàng cũng là sự thành công của chúng tôi.</div>

<div style="text-align: justify;"> </div>

<div style="text-align: justify;"><strong>2. Mục tiêu với đối tác:</strong></div>

<div style="text-align: justify;">Hiện nay, Triplen Watch Store đang phân phối các thương hiệu đồng hồ danh tiếng trên thế giới như: <b>Casio, Citizen, Seiko, Orient, DW</b>... Với mục tiêu hàng hóa phục vụ đa dạng, mẫu mã mới nhất, chất lượng tốt nhất, giá cả cạnh tranh nhất, Triplen Watch Store hiểu được tầm quan trọng của việc xây dựng các mối quan hệ và đạt được sự ủng hộ của những nhà cung cấp, những đối tác hàng đầu thế giới.</div>

<div style="text-align: justify;"> </div>

<div style="text-align: justify;">Triplen Watch Store mong muốn tìm kiếm những đối tác tiềm năng có khả năng cũng cấp những mẫu đồng hồ mới nhất để thiết lập mối quan hệ hợp tác trong tinh thần tất cả các bên cùng có lợi và cùng phát triển. Thành công của khách hàng là tương lai của công ty. <span style="line-height:1.6">Những yếu tố trên luôn gắn liền với truyền thống, uy tín và thương hiệu của Công ty Cổ phần </span><span style="line-height:1.6">Trực tuyến Vĩnh Khang trên thị trường Việt Nam cũng như với quốc tế.</span></div>

